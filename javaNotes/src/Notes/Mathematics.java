package Notes;
public class Mathematics {
///////////////////////////////////////////////////////////////////
//                         Math                                 //
/////////////////////////////////////////////////////////////////
// Math.abs() returns the absolute(+) value of its parameter()
	
	int a = Math.abs(10);     // 10
	int b = Math.abs(-20);   // 20
	
// Math.ceil() rounds UP the value as a double
	
	double c = Math.ceil(7.235);   // 8.0
	
// Math.floor() rounds DOWN the value as a double
	
	double d = Math.floor(7.235);  // 7.0

// Math.max() returns the largest of its parameter
	
	int e = Math.max(10, 20);  // 20
	
// Math.min() returns smallest of its parameter
	
	int f = Math.min(10, 20);  // 10
	
// Math.pow()  returns the first parameter raised to the power* of the second parameter
	
	double g = Math.pow(6, 2); // 36.0

}